                    <p style="text-align:justify;">إن للشهيد عند الله عز وجل منزلة عالية، ومكانة سامية، وثوابًا عظيمًا، وفضلًا كبيرًا؛<span style="color:rgb(0,204,255);"> (وَلاَ تَحْسَبَنَّ الَّذِينَ قُتِلُواْ فِي سَبِيلِ اللّهِ أَمْوَاتاً بَلْ أَحْيَاء عِندَ رَبِّهِمْ يُرْزَقُونَ {169} فَرِحِينَ بِمَا آتَاهُمُ اللّهُ مِن فَضْلِهِ وَيَسْتَبْشِرُونَ بِالَّذِينَ لَمْ يَلْحَقُواْ بِهِم مِّنْ خَلْفِهِمْ أَلاَّ خَوْفٌ عَلَيْهِمْ وَلاَ هُمْ يَحْزَنُونَ) (آل عمران)</span>.</p>
                    <p style="text-align:justify;">وصحَّ عن المقدام بن معد يكرب قال: قال رسول الله صلى الله عليه وسلم: «للشهيد عند الله سبع خصال: يغفر له في أول دفعة من دمه، ويرى مقعده من الجنة، ويُحلَّى حلَّة الإيمان، ويزوَّج اثنتين وسبعين زوجة من الحور العين، ويجار من عذاب القبر، ويأمن من الفزع الأكبر، ويوضع على رأسه تاج الوقار، الياقوتةُ منه خيرٌ من الدنيا وما فيها، ويشفع في سبعين إنسانًا من أهل بيته» (رواه أحمد، والترمذي، وابن ماجه).</p>
                    <p style="text-align:justify;">وعن أبي هريرة قال: قال رسول الله صلى الله عليه وسلم: «ما يجد الشهيد من مسِّ القتل، إلا كما يجد أحدكم من مسِّ القرصة».</p>
                    <p style="text-align:justify;">والسؤال الذي يطرح نفسه هنا: إذا كان الشهيد في هذه الدرجة الرفيعة، وإذا كان قد ارتقى إلى الله شهيدًا، فلماذا تنال منه أيدي الأعداء؟! ولماذا تتركهم السماء تنهشهم الكلاب؟! ولماذا لا تحفظهم عناية الرحمن من عبث الطغيان، وبطش الغلمان؟!</p>
                    <p style="text-align:justify;">أولاً: دفن الكافر فرض واجب في دين الإسلام، حتى لو قُتل وهو يحارب المسلمين، وحتى لو تم إعدامه بسبب الخيانة العظمى للوطن الذي يعيش فيه، ولا يجوز شرعاً التمثيل بجثمان القتيل حتى لو كان كافراً.</p>
                    <p style="text-align:justify;">فبعد انتهاء غزوة «بدر»، أمر النبي صلى الله عليه وسلم بدفن جثامين قتلى كفار قريش، ولم يمثّل بهم، ولم يتركهم للكلاب تأكلهم، أو تنهشهم.</p>
                    <p style="text-align:justify;">ثانياً: في غزوة «الأحزاب»، قام يهود بني قريظة بطعن المسلمين من الخلف، وتحالفوا مع الأعداء الذين جاؤوا من كل فج عميق، وضربوا حصاراً قاتلاً على مدينة رسول الله صلى الله عليه وسلم، وكادوا أن يبيدوها عن بكرة أبيها، وفي هذا الوقت العصيب، فعل يهود بني قريظة فعلتهم، وخانوا الوطن، لكن خاب مسعاهم، وانتهت المعركة بهزيمة الأحزاب، وانتصار المسلمين، فكان لا بد من محاكمة الخونة.</p>
                    <p style="text-align:justify;">وعقد النبي صلى الله عليه وسلم محاكمة عادلة للخونة الذين خانوا الوطن خيانة عظمى، وفوض للخونة حق اختيار القاضي الذي يرأس المحكمة، فاختاروا سعداً بن معاذ، الذي حكم بقتل الرجال المقاتلين فقط، وعفا عن النساء والصبيان، وبعد تنفيذ حكم الإعدام، أمر النبي صلى الله عليه وسلم بدفن جثث الخونة بعد إعدامهم، ولم يُمثّل بهم، ولم يتركهم في العراء طعاماً للكلاب.</p>
                    <p style="text-align:justify;">ثالثاً: هذه حضارة الإسلام، وهذه رحمة المسلمين بالأعداء المحاربين، وبالخونة المنافقين، التي لم تعرفها أوروبا ولا أمريكا ولا العالم أجمع حتى يومنا هذا؛ ما يؤكد بما لا يدع مجالاً للشك أن الإسلام سبيل الخلاص الوحيد للبشرية من كل ما تعانيه وتكابده من غدرات وخصومات وويلات.</p>
                    <p style="text-align:justify;">رابعاً: إن حياة الإنسان أغلى ما يملك، وإذا كان الإنسان قد بذل حياته رخيصة في سبيل مرضاة ربه، فلا يضر الشاة سلخها بعد ذبحها، أما التمثيل بجثث الشهداء، وعدم دفنها، وعدم تمكُّن البعض من الصلاة عليها، ومواراتها الثرى، وربما تنهشها الكلاب، فمن المعلوم أن كل أذى ينزل بجثمان الشهيد هو أجر جديد، وإذا مَثَّل الأعداء بالجثمان فهذا أجر فوق الشهادة، وإذا ترك الجثمان حتى تحلل وفاحت منه رائحة كريهة، فهذا أجر فوق الشهادة، وإذا لم يجد الشهيد من يواريه الثرى فهذا أجر شهادة أخرى؛ لأن الله عز وجل أخبرنا أن «ما يصيب المسلم من هَمٍّ أو غَمٍّ أو أذًى إلا كفَّر الله عنه من خطاياه».</p>
                    <p style="text-align:justify;">فما ينزل بجثامين الشهداء من بلاء يضاعف الأجر والثواب، ويُكفِّر السيئات، ويرفع الدرجات، ويباعد من الخطيئات، ويضاعف الحسنات.</p>
                    <p style="text-align:justify;">خامساً: لا يضير الشهيد أن تنهش جسده الكلاب، فهذا لا ينقص من منزلته عند ربه، عن أنس بن مالك رضي الله عنه، أن رسول الله صلى الله عليه وسلم مرّ على حمزة رضي الله عنه وقد مُثّل به، فقال: «لولا أن تجد صفية في نفسها لتركته حتى تأكله العافية حتى يُحشر من بطونها»؛ والعافية: السباع والطير التي تقع على الجيف فتأكلها.</p>
                    <p style="text-align:justify;">وأما هو فعند ربه روحه في جوف طير خضر ترد أنهار الجنة، تأكل من ثمارها، وتأوي إلى قناديل من ذهب معلقة في ظل العرش.</p>
                    <p style="text-align:justify;">سادساً: أما ما يفعله الاحتلال من ترك جثامين الشهداء تأكلها الكلاب فهو جريمة ضد الإنسانية، وما أكثر جرائمهم! وقد صدق الله تعالى فيهم:&nbsp;<span style="color:rgb(0,204,255);">(ثُمَّ قَسَتْ قُلُوبُكُم مِّن بَعْدِ ذَلِكَ فَهِيَ كَالْحِجَارَةِ أَوْ أَشَدُّ قَسْوَةً) (البقرة: 74)</span>، وهذا جرم كبير، وإثم عظيم، وطغيان خطير يضاف إلى سلاسل جرائمهم التي لا تنتهي في حق البشر والشجر والحجر.</p>
